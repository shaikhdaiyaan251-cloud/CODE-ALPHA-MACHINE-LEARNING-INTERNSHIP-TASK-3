        if pred_class == 1:
            md = f"## 🩺 Prediction: **DISEASE PRESENT** (Confidence: {disease_prob:.1f}%)"
        else:
            md = f"## ✅ Prediction: **NO DISEASE** (Confidence: {no_disease_prob:.1f}%)"
        display(Markdown(md))

        # Gauge chart
        fig, ax = plt.subplots(figsize=(6, 2))
        ax.barh(['Disease'], [disease_prob], color='crimson', alpha=0.7)
